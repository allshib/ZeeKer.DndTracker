        public FillFromClassController()
        {
            InitializeComponent();
            TargetObjectType=typeof(Character);

            var action = new SimpleAction(this, "FillFromClass", PredefinedCategory.Edit)
            {
                Caption = "Заполнить из класса"
            };

            action.Execute += Action_Execute;
        }

        private void Action_Execute(object sender, SimpleActionExecuteEventArgs e)
        {
            var character = View.CurrentObject as Character;

            character.Class.DefaultStorage.CustomMapTo(character.LocalStorage);
